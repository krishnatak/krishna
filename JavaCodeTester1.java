public class JavaCodeTester1{

	public static void main(String[]args){


		// this variable stored a boolean value
		boolean accountCreated = true;
		System.out.println(accountCreated); 


		// this variable stored a character
		char averageGrade = 'C';
		System.out.println(averageGrade);


  		//these variables store strings
		String firstName = "Krishna " ;
		String lastName = "Tak";
		System.out.println(firstName+lastName);


		// this variable holds an Integer
		int ieltsBand = 7;
		System.out.println(ieltsBand);


		//this variable holds a double data type
		double formInput = 12.9d;
		System.out.println(formInput);

	}
}
